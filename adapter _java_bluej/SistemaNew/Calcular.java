
/**
 * Classe utilizada para efetuar os cálculos
 *
 * @author (Angelo Rafael)
 * @version (1.0)
 */
public class Calcular
{

    /** 
     * Construtor da Classe
     */
    public Calcular() {
        
    }
        
    /**
     * Função que soma os elementos
     *
     * @param  a  entrada de dados (inteiro)
     * @param  b  entrada de dados (inteiro)
     * @return    a soma dos dados (a) e (b)
     */
    public int somar(int a, int b)
    {
        return a + b;
    }

    public int subtrair(int a, int b)
    {
        return a - b;
    }
   
}
